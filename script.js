//  POPUP CHI TIẾT MÔN HỌC
// ══════════════════════════════════════════════════
function showSubjectDetail(mon) {
  const { info } = _currentSubjectMap[mon] || {};
  if (!info) return;

  // Môn không có lịch trong tuần
  if (!info.time || info.time === 'root') {
    const html = `
      <div class="modal-header">
        <div class="modal-title">📖 ${mon}</div>
        <div class="modal-subtitle">Chi tiết môn học</div>
      </div>
      <div class="modal-body">
        <div style="text-align:center;padding:24px 0;color:var(--text-muted);font-size:0.95rem;">
          📭 Trong tuần không có tiết môn <strong>${mon}</strong>
        </div>
      </div>`;
    openModal(html);
    return;
  }

  const slots = parseTime(info.time);
  const gvDisplay = (info.gv === 'root' || !info.gv) ? '—' : info.gv;
  const roomDisplay = Array.isArray(info.room) ? '—' : info.room;

  const slotsHtml = slots.length
    ? slots.map(s => `<span class="modal-slot">📅 ${s.session} · ${s.day} · ${s.tiet}</span>`).join('')
    : '<span style="color:var(--text-muted)">—</span>';

  const html = `
    <div class="modal-header">
      <div class="modal-title">📖 ${mon}</div>
      <div class="modal-subtitle">Chi tiết môn học</div>
    </div>
    <div class="modal-body">
      <div class="modal-row">
        <span class="modal-row-icon">👤</span>
        <span class="modal-row-label">Giáo viên</span>
        <span class="modal-row-value">${gvDisplay}</span>
      </div>
      <div class="modal-row">
        <span class="modal-row-icon">📍</span>
        <span class="modal-row-label">Phòng</span>
        <span class="modal-row-value">
          ${roomDisplay !== '—' ? `<span class="modal-room-badge">🚪 P. ${roomDisplay}</span>` : '—'}
        </span>
      </div>
      <div class="modal-row">
        <span class="modal-row-icon">🕐</span>
        <span class="modal-row-label">Lịch học</span>
        <span class="modal-row-value">${slotsHtml}</span>
      </div>
    </div>`;

  openModal(html);
}

// ══════════════════════════════════════════════════
//  BUILD TIMETABLE GRID
// ══════════════════════════════════════════════════
function buildTimetableGrid(subjectMap, selectedClass) {
  const grid = {};
  SESSIONS.forEach(s => { grid[s] = {}; DAYS_ORDER.forEach(d => { grid[s][d] = {}; }); });

  Object.entries(subjectMap).forEach(([mon, { info, ttClass }]) => {
    const slots = parseTime(info.time);
    slots.forEach(({ session, day, tiet }) => {
      if (!grid[session] || !grid[session][day]) return;
      if (!grid[session][day][tiet]) grid[session][day][tiet] = [];
      grid[session][day][tiet].push({ mon, ttClass });
    });
  });

  // Nếu Thứ7 - Buổi sáng - Tiết5 trống thì điền SHCN
  const thu7SangTiet5 = grid['Buổi sáng']['Thứ7']['Tiết5'];
  if (!thu7SangTiet5 || thu7SangTiet5.length === 0) {
    grid['Buổi sáng']['Thứ7']['Tiết5'] = [{ mon: '__SHCN__', ttClass: 'tt-phu' }];
    // Đảm bảo Thứ7 xuất hiện trong activeDays
    if (!subjectMap['__SHCN__']) {
      subjectMap['__SHCN__'] = {
        info: { gv: 'GVCN', time: 'Buổi sáng-Thứ7-Tiết5', room: selectedClass },
        ttClass: 'tt-phu'
      };
    }
  }

  const activeDays = DAYS_ORDER.filter(d =>
    SESSIONS.some(s => Object.values(grid[s][d]).some(arr => arr.length > 0))
  );

  if (activeDays.length === 0) return '<p style="color:var(--text-muted)">Không có dữ liệu.</p>';

  let html = '<div class="timetable-wrapper"><table class="timetable"><thead><tr>';
  html += '<th>Tiết</th>';
  activeDays.forEach(d => { html += `<th>${DAYS_LABEL[d] || d}</th>`; });
  html += '</tr></thead><tbody>';

  SESSIONS.forEach(session => {
    const hasSession = activeDays.some(d =>
      Object.values(grid[session][d]).some(arr => arr.length > 0)
    );
    if (!hasSession) return;

    const icon = session === 'Buổi sáng' ? '☀️' : '🌙';
    html += `<tr><td class="session-label" colspan="${activeDays.length + 1}">${icon} ${session}</td></tr>`;

    for (let t = 1; t <= TIET_COUNT; t++) {
      const tietKey = `Tiết${t}`;
      html += `<tr><td>Tiết ${t}</td>`;
      activeDays.forEach(d => {
        const cells = grid[session][d][tietKey] || [];
        if (cells.length === 0) {
          html += '<td></td>';
        } else {
          html += '<td>';
          cells.forEach(({ mon, ttClass }) => {
            if (mon === '__SHCN__') {
              html += `<div class="tt-cell tt-phu">
                <span class="tt-subject">SHCN</span>
                <span class="tt-room">P. ${selectedClass}</span>
              </div>`;
              return;
            }
            const info = subjectMap[mon] ? subjectMap[mon].info : null;
            const roomStr = info && !Array.isArray(info.room) ? `P. ${info.room}` : '';
            html += `<div class="tt-cell ${ttClass}" onclick="showSubjectDetail('${mon.replace(/'/g, "\\'")}')">
              <span class="tt-subject">${mon}</span>
              ${roomStr ? `<span class="tt-room">${roomStr}</span>` : ''}
            </div>`;
          });
          html += '</td>';
        }
      });
      html += '</tr>';
    }
  });

  html += '</tbody></table></div>';
  return html;
}

