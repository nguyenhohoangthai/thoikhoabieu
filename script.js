// ══════════════════════════════════════════════════
//  PARSE TIME STRING → [{session, day, tiet}]
// ══════════════════════════════════════════════════
function parseTime(timeStr) {
  if (!timeStr || timeStr === 'root') return [];
  return timeStr.split(';').map(t => {
    const parts = t.split('-');
    return { session: parts[0], day: parts[1], tiet: parts[2] };
  });
}

