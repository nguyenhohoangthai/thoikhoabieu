function handleSubmit(e) {
  if (e) e.preventDefault();

  const selectedClass = document.getElementById('class').value;
  const optionalCheckboxes = document.querySelectorAll('input.optional-subject[type="checkbox"]:checked');
  const selectedSubjects = Array.from(optionalCheckboxes).map(cb => cb.value);
  const classData = timetableData[selectedClass];

  const resultSection = document.getElementById('result-section');

  // 4 môn chính
  const mainSubjects = ['Toán', 'Ngữ văn', ...selectedSubjects];
  const colorMap = {
    'Toán':    { tt: 'tt-toan' },
    'Ngữ văn': { tt: 'tt-van'  },
  };
  selectedSubjects.forEach((mon, i) => {
    colorMap[mon] = { tt: i === 0 ? 'tt-tc' : 'tt-tc2' };
  });

  // Subject map (4 môn chính + môn phụ)
  const allForTT = {};
  mainSubjects.forEach(mon => {
    allForTT[mon] = { info: classData[mon], ttClass: colorMap[mon].tt };
  });
  OTHER_SUBJECTS.forEach(mon => {
    const info = classData[mon];
    if (info && info.time !== 'root') {
      allForTT[mon] = { info, ttClass: 'tt-phu' };
    }
  });

  // Lưu trạng thái toàn cục để dùng khi click popup
  _currentSubjectMap = allForTT;
  _currentColorMap   = colorMap;

  const ttHtml = buildTimetableGrid(allForTT, selectedClass);
  const detailHtml = buildSubjectDetailList(allForTT, mainSubjects);

  resultSection.style.display = 'block';
  resultSection.innerHTML = `
    <div class="result-header">
      <div class="result-header-info">
        <span style="font-size:1.2rem">📅</span>
        <h2>Lớp ${selectedClass} &nbsp;·&nbsp; ${mainSubjects.join(' · ')}</h2>
      </div>
      <button class="btn-save" id="btn-save-excel" onclick='exportToExcel("${selectedClass}")'>
        Xuất thời khóa biểu
        </button>
    </div>
    <div class="result-body">
      <div id="timetable-capture">
        <div style="padding:14px 0 10px;font-size:0.78rem;color:var(--text-muted);font-weight:600;">
          💡 Nhấn vào ô môn học để xem chi tiết giáo viên &amp; phòng học
        </div>
        ${ttHtml}
        ${detailHtml}
      </div>
    </div>`;

  resultSection.scrollIntoView({ behavior: 'smooth', block: 'start' });
}
